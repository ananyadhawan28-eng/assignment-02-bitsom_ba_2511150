-- ============================================================
-- TABLE 4: orders
-- The only table that changes per transaction.
-- References customers, products, sales_reps via FK.
-- total_price = unit_price * quantity (computed at load time).
-- ============================================================
CREATE TABLE orders (
    order_id      VARCHAR(10)   NOT NULL,
    customer_id   VARCHAR(10)   NOT NULL,
    product_id    VARCHAR(10)   NOT NULL,
    quantity      INT           NOT NULL,
    total_price   DECIMAL(10,2) NOT NULL,
    order_date    DATE          NOT NULL,
    sales_rep_id  VARCHAR(10)   NOT NULL,
    CONSTRAINT pk_orders      PRIMARY KEY (order_id),
    CONSTRAINT fk_customer    FOREIGN KEY (customer_id)  REFERENCES customers(customer_id),
    CONSTRAINT fk_product     FOREIGN KEY (product_id)   REFERENCES products(product_id),
    CONSTRAINT fk_sales_rep   FOREIGN KEY (sales_rep_id) REFERENCES sales_reps(sales_rep_id)
);

